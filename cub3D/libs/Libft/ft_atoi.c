#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				sign;
	long long int	nb;

	i = 0;
	sign = 1;
	nb = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = sign * (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (nb + str[i] - '0' != (nb + str[i] - '0') * 10 / 10)
			return ((sign + 1) / -2);
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return ((int)nb * sign);
}
